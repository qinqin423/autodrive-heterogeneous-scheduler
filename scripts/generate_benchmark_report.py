from __future__ import annotations

import csv
import json
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt

from autodrive_scheduler.schedulers import EDFScheduler, FIFOScheduler, FixedScheduler
from autodrive_scheduler.simulator import run_from_file


PROJECT_ROOT = Path(__file__).resolve().parents[1]
SCENARIOS = [
    PROJECT_ROOT / "configs" / "scenarios" / "high_load.yaml",
    PROJECT_ROOT / "configs" / "scenarios" / "emergency_overload.yaml",
]
SCHEDULER_TYPES = (FixedScheduler, FIFOScheduler, EDFScheduler)

RAW_OUTPUT_ROOT = PROJECT_ROOT / "results" / "generated" / "benchmark" / "raw"
SUMMARY_CSV = PROJECT_ROOT / "results" / "generated" / "benchmark" / "summary.csv"
BENCHMARK_MD = PROJECT_ROOT / "docs" / "benchmark_results.md"
ASSETS_DIR = PROJECT_ROOT / "docs" / "assets"
PLOT_PATH = ASSETS_DIR / "emergency_deadline_miss_rate.png"


DETERMINISTIC_FIELDS = [
    "scenario",
    "scheduler",
    "completed_tasks",
    "deadline_misses",
    "deadline_miss_rate",
    "mean_response_ms",
    "p95_response_ms",
    "throughput_tasks_per_s",
    "observed_duration_ms",
]


def run_experiments() -> list[dict[str, object]]:
    summaries = []
    for scenario_path in SCENARIOS:
        scenario_name = scenario_path.stem
        for scheduler_type in SCHEDULER_TYPES:
            scheduler = scheduler_type()
            raw_output = RAW_OUTPUT_ROOT / scenario_name / scheduler.name
            summary = run_from_file(scenario_path, scheduler, raw_output)
            summaries.append(summary)
    return summaries


def write_summary_csv(summaries: list[dict[str, object]]) -> None:
    SUMMARY_CSV.parent.mkdir(parents=True, exist_ok=True)
    rows = []
    for summary in summaries:
        row = {field: summary[field] for field in DETERMINISTIC_FIELDS}
        rows.append(row)
    with SUMMARY_CSV.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=DETERMINISTIC_FIELDS)
        writer.writeheader()
        writer.writerows(rows)


def generate_markdown_report(summaries: list[dict[str, object]]) -> str:
    high_load = [s for s in summaries if s["scenario"] == "high_load"]
    emergency = [s for s in summaries if s["scenario"] == "emergency_overload"]

    fifo_misses = next(s["deadline_misses"] for s in emergency if s["scheduler"] == "fifo")
    edf_misses = next(s["deadline_misses"] for s in emergency if s["scheduler"] == "edf")
    if fifo_misses <= 0:
        raise ValueError(
            f"Cannot compute EDF reduction: FIFO deadline misses ({fifo_misses}) must be positive. "
            "This scenario does not produce deadline misses under FIFO, so the EDF reduction metric is not applicable."
        )
    reduction = ((fifo_misses - edf_misses) / fifo_misses) * 100

    fifo_p95 = next(s["p95_response_ms"] for s in emergency if s["scheduler"] == "fifo")
    edf_p95 = next(s["p95_response_ms"] for s in emergency if s["scheduler"] == "edf")
    fifo_mean = next(s["mean_response_ms"] for s in emergency if s["scheduler"] == "fifo")
    edf_mean = next(s["mean_response_ms"] for s in emergency if s["scheduler"] == "edf")

    high_load_fifo_misses = next(s["deadline_misses"] for s in high_load if s["scheduler"] == "fifo")
    high_load_edf_misses = next(s["deadline_misses"] for s in high_load if s["scheduler"] == "edf")

    lines = [
        "<!-- Generated by scripts/generate_benchmark_report.py. Do not edit manually. -->",
        "",
        "# Benchmark Results",
        "",
        "## Experiment Objectives",
        "",
        "This benchmark evaluates three scheduling policies under controlled high-load and emergency overload scenarios:",
        "",
        "- **Fixed**: Static resource binding based on service preference.",
        "- **FIFO**: First-come first-served with earliest-finish resource selection.",
        "- **EDF**: Non-preemptive Earliest Deadline First within each device's wait queue.",
        "",
        "## Controlled Variable Setup",
        "",
        "Two deterministic scenarios form a controlled variable experiment:",
        "",
        "- **`high_load`**: Near-saturation background workload without emergency requests.",
        "- **`emergency_overload`**: Same background workload with periodic emergency obstacle detection added.",
        "",
        "The only workload-level change is the addition of periodic emergency perception requests; device profiles, service profiles, duration, random seed, and background workloads remain unchanged.",
        "",
        "## `high_load` Results",
        "",
        "| Scheduler | Completed | Misses | Miss Rate | Mean Response (ms) | P95 Response (ms) | Throughput (tasks/s) | Duration (ms) |",
        "|-----------|-----------|--------|-----------|-------------------|------------------|----------------------|---------------|",
    ]
    scheduler_display = {"fixed": "Fixed", "fifo": "FIFO", "edf": "EDF"}
    for s in high_load:
        lines.append(
            f"| {scheduler_display[s['scheduler']]} | {s['completed_tasks']} | {s['deadline_misses']} | "
            f"{s['deadline_miss_rate']:.1%} | {s['mean_response_ms']:.1f} | {s['p95_response_ms']} | "
            f"{s['throughput_tasks_per_s']:.1f} | {s['observed_duration_ms']} |"
        )
    lines.extend([
        "",
        "## `emergency_overload` Results",
        "",
        "| Scheduler | Completed | Misses | Miss Rate | Mean Response (ms) | P95 Response (ms) | Throughput (tasks/s) | Duration (ms) |",
        "|-----------|-----------|--------|-----------|-------------------|------------------|----------------------|---------------|",
    ])
    for s in emergency:
        lines.append(
            f"| {scheduler_display[s['scheduler']]} | {s['completed_tasks']} | {s['deadline_misses']} | "
            f"{s['deadline_miss_rate']:.1%} | {s['mean_response_ms']:.1f} | {s['p95_response_ms']} | "
            f"{s['throughput_tasks_per_s']:.1f} | {s['observed_duration_ms']} |"
        )
    lines.extend([
        "",
        "## Key Conclusions",
        "",
        f"- Under `high_load`, both FIFO and EDF achieve zero deadline misses ({high_load_fifo_misses} and {high_load_edf_misses} respectively).",
        f"- Under `emergency_overload`, EDF reduces deadline misses from {fifo_misses} to {edf_misses}, an {reduction:.1f}% reduction.",
        f"- EDF mean response ({edf_mean:.1f} ms) remains close to FIFO ({fifo_mean:.1f} ms).",
        f"- EDF shows a slight P95 latency cost ({edf_p95} ms vs FIFO {fifo_p95} ms) due to prioritizing short-deadline tasks.",
        "- Fixed static mapping produces significant deadline misses in both scenarios, demonstrating the limitation of fixed binding under load imbalance.",
        "",
        "## Reproduction",
        "",
        "```bash",
        "python -m pip install -e \".[benchmark]\"",
        "python scripts/generate_benchmark_report.py",
        "```",
        "",
        "## Notes",
        "",
        "- Device execution times are synthetic configurations and do not represent real in-vehicle hardware performance.",
        "- Wall-clock scheduler overhead is excluded from this report as it varies across machines and runs.",
    ])
    return "\n".join(lines) + "\n"


def generate_plot(summaries: list[dict[str, object]]) -> None:
    emergency = [s for s in summaries if s["scenario"] == "emergency_overload"]
    schedulers = ["Fixed", "FIFO", "EDF"]
    scheduler_names = ["fixed", "fifo", "edf"]
    miss_rates = [
        next(s["deadline_miss_rate"] for s in emergency if s["scheduler"] == name)
        * 100
        for name in scheduler_names
    ]

    fig, ax = plt.subplots(figsize=(10.2, 5.95))
    colors = ["#7A7A7A", "#0072B2", "#009E73"]

    ax.grid(axis="y", linestyle="-", alpha=0.3)
    bars = ax.bar(schedulers, miss_rates, color=colors, zorder=2)

    for bar, rate in zip(bars, miss_rates):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height(),
            f"{rate:.1f}%",
            ha="center",
            va="bottom",
            fontsize=10,
        )

    ax.set_ylabel("Deadline Miss Rate (%)")
    ax.set_title("Deadline Miss Rate under Emergency Overload")
    ax.set_ylim(0, max(miss_rates) * 1.2)
    ax.annotate(
        "Synthetic deterministic scenario; lower is better.",
        xy=(0.5, -0.15),
        xycoords="axes fraction",
        ha="center",
        fontsize=10,
        color="#666666",
    )

    plt.tight_layout()
    ASSETS_DIR.mkdir(parents=True, exist_ok=True)
    fig.savefig(PLOT_PATH, dpi=140, bbox_inches="tight")
    plt.close(fig)


def main() -> None:
    summaries = run_experiments()
    write_summary_csv(summaries)
    report = generate_markdown_report(summaries)
    BENCHMARK_MD.write_text(report, encoding="utf-8")
    generate_plot(summaries)
    print(json.dumps(summaries, indent=2, ensure_ascii=False))


if __name__ == "__main__":
    main()